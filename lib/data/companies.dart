/// Get company by ID
CompanyData? getCompanyById(String id) {
  try {
    return allCompanies.firstWhere((c) => c.id == id);
  } catch (_) {
    return null;
  }
}

/// Get company by ticker
CompanyData? getCompanyByTicker(String ticker) {
  try {
    return allCompanies.firstWhere((c) => c.ticker == ticker);
  } catch (_) {
    return null;
  }
}

/// Get companies by sector
List<CompanyData> getCompaniesBySector(String sectorId) {
  return allCompanies.where((c) => c.sectorId == sectorId).toList();
}

/// Get companies by tier
List<CompanyData> getCompaniesByTier(CompanyTier tier) {
  return allCompanies.where((c) => c.tier == tier).toList();
}

/// Get companies by sector and tier
List<CompanyData> getCompaniesBySectorAndTier(String sectorId, CompanyTier tier) {
  return allCompanies.where((c) => c.sectorId == sectorId && c.tier == tier).toList();
}

/// Get starter companies (unlocked by default)
List<CompanyData> getStarterCompanies() {
  return allCompanies.where((c) => c.tier == CompanyTier.starter).toList();
}

/// Get blue chip companies
List<CompanyData> getBlueChipCompanies() {
  return allCompanies.where((c) => c.isBlueChip).toList();
}

/// Get meme stocks
List<CompanyData> getMemeStocks() {
  return allCompanies.where((c) => c.isMemeStock).toList();
}

/// Get penny stocks
List<CompanyData> getPennyStocks() {
  return allCompanies.where((c) => c.isPennyStock).toList();
}
